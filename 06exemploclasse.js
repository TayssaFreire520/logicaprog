// Definindo uma classe chamada Pessoa

class Pessoa {
    nome;
    idade;
} // Fim da classe Pessoa

// Instanciando (criando) um objeto que é o pessoa1 da classe Pessoa
let pessoa1 = new Pessoa();

// Atribuindo valores aos atributos do objeto Pessoa1
pessoa1.nome = "João";
pessoa1.idade = 23;
console.log ( pessoa1 );

let pessoa2 = new Pessoa();
pessoa2.nome = "Ana";
pessoa2.idade = 30;
console.log ( pessoa2 );